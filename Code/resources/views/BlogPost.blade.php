                        <p>Hello to everyone,</p>
                        <p>this week was a special week for us. On Wednesday we got a visit from a woman of a bigger company. She is a professional scrum master and thought us how to do a retrospective of our agile work. Within this course she asked us three main questions regarding our scrum approach:</p>
                        <h5><span style="color: #000000;">What went well?</span></h5>
                        <ul>
                            <li><strong>Clear defined tasks:</strong>  Every team member knows exactly what to do</li>
                            <li><strong>Feedback of the current progress:</strong>  Every team member knows what the others are doing</li>
                            <li><strong>Good reliability: </strong>Every team member is doing their tasks continuously and on time</li>
                        </ul>
                        <h5><span style="color: #000000;">What can be done better?</span></h5>
                        <ul>
                            <li><strong>Time effort: </strong>It takes us to long to distribute the tasks</li>
                            <li><strong>Tool knowledge:</strong> To get to know the tools we are using better to use them in a more efficient way</li>
                        </ul>
                        <h5><span style="color: #000000;">What are your goals for how to improve?</span></h5>
                        <ul>
                            <li><strong>Use hints:</strong> The other teams provided the information about their task distribution: They are using &#8220;Round Robin&#8221; and experts for each area</li>
                            <li><strong>General team-member-exchange:</strong> Sharing knowledge e.g. the content of the e-Portfolio about Laravel one of our team member is presenting</li>
                        </ul>
                        <p>What do you think about our ideas. Do you have any additional measures we can take?</p>
                        <p>We are happy to hear about your opinion and are looking forward to your input.</p>
